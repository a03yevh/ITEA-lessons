package Lessons.Lesson9.ex1;

public class Calculator implements Interface1, Interface2, Interface3, Interface4 {

    @Override
    public int adding(int a, int b) {
        return a + b;
    }

    @Override
    public int minus(int a, int b) {
        return a - b;
    }

    @Override
    public int mult(int a, int b) {
        return a * b;
    }

    @Override
    public int div(int a, int b) {
        return a / b;
    }
}